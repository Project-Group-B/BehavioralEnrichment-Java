package com.uno.zoo.dto;

public class AnimalInfo {
	private int id;
	private int isisNumber;
	private String species;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIsisNumber() {
		return isisNumber;
	}
	public void setIsisNumber(int isisNumber) {
		this.isisNumber = isisNumber;
	}
	public String getSpecies() {
		return species;
	}
	public void setSpecies(String name) {
		this.species = name;
	}
}
